# For-løkker

for i in range(1, 16):
    print(i, end=" ")
    
print("\n")
    
for i in range(1, 16):
    print(i)
    
print("\n")
    
for i in range(1, 16, 2):
    print(i, end=" ")

print("\n")
    
    
# While-løkker

i = 1
while i < 16:
    print(i, end=" ")
    i += 1
    
print("\n")
    
i = 1
while i < 16:
    print(i)
    i += 1
    
print("\n")
    
