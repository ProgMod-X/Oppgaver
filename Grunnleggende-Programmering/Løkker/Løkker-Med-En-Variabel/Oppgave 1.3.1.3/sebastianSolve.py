'''
En for-løkke fullfører en algoritme en bestemt antall ganger.

While-løkken fulfører algoritmen til en condition ikke lenger er sant. 

'''

for x in range(1, 11):
    print(x)
    
print("\n")

y = 1

while y <= 10:
    print(y)
    y += 1
    